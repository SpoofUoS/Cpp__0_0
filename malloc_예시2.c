#include<stdio.h>
#include<stdlib.h>
#include<string.h>

typedef struct studentTag {
    char name[10]; // 문자배열로 된 이름
    int age;       // 나이를 나타내는 정수값
    double gpa;    // 평균 평점을 나타내는 실수값
} student;

int main()
{
    student *s;  // 구조체를 가리키는 포인터

    s = (student *)malloc(sizeof(student));

    if (s == NULL) {
        fprintf(stderr, "NULL. \n");
        exit(1);
    }

    strcpy(s->name, "Park");  // (*s).name으로도 가능하다.
    s->age = 20;

    free(s);
}